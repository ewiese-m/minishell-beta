#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	unsigned char	*ptr;
	size_t			total;

	total = nmemb * size;
	if (total != 0 && total / size != nmemb)
		return (NULL);
	ptr = malloc(total);
	if (ptr)
		ft_bzero(ptr, nmemb * size);
	return (ptr);
}
