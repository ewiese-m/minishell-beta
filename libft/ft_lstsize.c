#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*tmp;
	int		c;

	tmp = lst;
	c = 0;
	while (tmp)
	{
		tmp = tmp->next;
		c++;
	}
	return (c);
}
